// 根据区域和结果类型获取随机事件
export function getAlmsEvent(areaId: string, resultType: string): { scene: string, character: string, event: string } | null {
    const areaData = V19_LIB[areaId];
    if (!areaData) return null;
    
    // 根据结果类型选择分类
    const categories = Object.keys(areaData);
    let category: string;
    
    if (resultType === 'JP' || resultType === 'W2') {
        // 大吉 - 温情治愈 或 穿越混搭
        category = Math.random() > 0.5 ? '温情治愈' : '穿越混搭';
    } else if (resultType === 'BW') {
        // 小吉 - 玩梗搞笑
        category = '玩梗搞笑';
    } else if (resultType === 'NM') {
        // 平 - 随机
        category = categories[Math.floor(Math.random() * categories.length)];
    } else if (resultType === 'SW') {
        // 小亏 - 荒诞反转 或 悬疑神秘
        category = Math.random() > 0.5 ? '荒诞反转' : '悬疑神秘';
    } else {
        // 大凶 - 悬疑神秘 或 荒诞反转
        category = Math.random() > 0.5 ? '悬疑神秘' : '荒诞反转';
    }
    
    const categoryData = areaData[category];
    if (!categoryData) return null;
    
    const scenes = categoryData.s;
    const characters = categoryData.c;
    const events = categoryData.t;
    
    const index = Math.floor(Math.random() * scenes.length);
    
    return {
        scene: scenes[index],
        character: characters[index],
        event: events[index]
    };
}

// 根据区域生成化缘intro文本
export function getAlmsIntro(areaId: string): string {
    // 优先使用完整故事
    const story = getAlmsStory(areaId);
    if (story) {
        _currentStory = story;
        return story.intro;
    }
    
    // fallback: 旧的随机拼接
    _currentStory = null;
    const eventData = getAlmsEvent(areaId, 'NM');
    if (!eventData) return '你来到了一个神秘的地方...';
    
    // 使用和HTML版一致的拼接格式
    return `你来到了「${eventData.scene}」。\n\n只见「${eventData.character}」就在眼前，${eventData.event}...\n\n你该如何抉择？`;
}

