// 根据结果类型获取resultText
export function getAlmsResultText(resultKey: string, eventData: any, choice: string = 'safe'): string {
    // 优先使用完整故事的结局
    if (_currentStory && _currentStory.results && _currentStory.results[resultKey]) {
        return _currentStory.results[resultKey];
    }
    
    // fallback: 过渡文字 + 结果模板
    const transTexts = TRANS_TEXTS[choice] || TRANS_TEXTS.safe;
    const resTexts = RES_TEXTS[resultKey] || ['事情并没有如你所愿...'];
    
    const trans = transTexts[Math.floor(Math.random() * transTexts.length)];
    const res = resTexts[Math.floor(Math.random() * resTexts.length)];
    
    return trans + '\n\n' + res;
}
